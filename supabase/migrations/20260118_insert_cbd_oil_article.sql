## Benefits of CBD Oil

CBD oil remains the most popular CBD format in Europe for several reasons:

### Flexible Dosing
Unlike pre-measured gummies or capsules, oil lets you adjust your dose drop by drop. This is valuable when finding your optimal amount through [titration](/glossary/titration)—starting low and increasing gradually.

### Faster Onset Than Edibles
Sublingual absorption delivers effects in 15-30 minutes compared to 30-60+ minutes for [CBD edibles](/glossary/cbd-edibles) that must pass through digestion.

### Higher Bioavailability
At 20-30%, sublingual CBD oil delivers more CBD to your system than edibles (10-20% bioavailability) for the same milligram dose.

### Cost-Effective
Milligram for milligram, CBD oil typically costs less than gummies or capsules. A 1000mg bottle provides measurable doses without the manufacturing costs of individual edibles.

### Versatile Use
Beyond sublingual use, CBD oil can be:
- Added to food or drinks
- Applied topically for localised use
- Mixed into homemade edibles

### Full-Spectrum Options Available
Oil formats readily accommodate full-spectrum extracts, giving access to the [entourage effect](/glossary/entourage-effect) that isolate-based products may lack.

## Drawbacks of CBD Oil

No product is perfect. Here''s where CBD oil falls short:

### Taste
Natural CBD oil has an earthy, grassy, sometimes bitter flavour that many people dislike. Full-spectrum oils with more plant compounds tend to taste stronger than isolates. Flavoured options help but don''t eliminate this entirely.

### Inconvenience
Using oil requires finding a private moment to hold liquid under your tongue, carrying a glass bottle with risk of breakage, and counting drops or measuring carefully.

### Dosing Variability
While flexibility is an advantage, it''s also a drawback. Each dropper may not contain exactly the same amount, and user error in measuring is common. Capsules and gummies offer more consistent dosing.

### Shorter Duration
The 4-6 hour duration means you may need multiple doses daily, while slow-release capsules or edibles can last longer.

### Not Always Travel-Friendly
Glass bottles, liquid restrictions on flights, and the appearance of "cannabis oil" can complicate travel, especially internationally.

## How to Use CBD Oil

### Step-by-Step Instructions

1. **Shake the bottle** — Cannabinoids can settle; shaking ensures even distribution.

2. **Fill the dropper** — Squeeze the rubber top, insert into oil, release to fill. Note where the oil level sits (most droppers have measurement markings).

3. **Dispense under tongue** — Lift your tongue and squeeze the dropper to release oil into the sublingual space.

4. **Hold for 60-90 seconds** — This allows absorption through the mucosa. Resist swallowing immediately.

5. **Swallow the remainder** — Any unabsorbed oil will pass through digestion for slower secondary absorption.

### Finding Your Dose

There''s no universal CBD dose. Factors affecting your ideal amount include body weight, individual metabolism, what you''re using it for, and CBD concentration in your product.

**Starting point:** Most guidance suggests beginning with 10-25mg of CBD and adjusting from there. This approach is called [titration](/glossary/titration)—the "start low and go slow" method.

**Calculate your dose:**
- Check the total CBD in the bottle (e.g., 1000mg)
- Check the bottle size (e.g., 30ml)
- Divide to find CBD per ml (1000÷30 = 33mg per ml)
- A standard dropper holds approximately 1ml

Use our [CBD dosage calculator](/tools/dosage-calculator) for personalised guidance based on your weight and goals.


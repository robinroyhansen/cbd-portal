## What to Look for When Buying CBD Oil

### Third-Party Lab Testing (COA)

The most important quality indicator. A [Certificate of Analysis](/glossary/certificate-of-analysis) from an independent lab should confirm:
- Actual CBD content matches the label
- THC levels are under 0.2% (EU legal limit)
- No heavy metals, pesticides, or solvents
- No microbial contamination

Reputable brands publish COAs on their website or provide them on request. Avoid any brand that can''t show [third-party testing](/glossary/third-party-testing).

### Extraction Method

**[CO2 extraction](/glossary/co2-extraction)** is the gold standard—clean, efficient, and preserves cannabinoids well. Some brands use [ethanol extraction](/glossary/ethanol-extraction), which is also acceptable. Avoid products that don''t disclose their extraction method.

### Hemp Source

European-grown hemp under EU regulations offers quality assurance. Look for organic certification where possible. Brands should disclose where their hemp is sourced.

### Carrier Oil

- **MCT oil** — Fast absorption, neutral taste
- **Hemp seed oil** — Additional omega fatty acids, stronger taste
- **Olive oil** — Familiar taste, good absorption

None is objectively "better"—choose based on preference.

### Concentration

Match concentration to your needs:
- **Low (250-500mg per bottle)** — Beginners, light use
- **Medium (500-1000mg)** — Regular users
- **High (1500-3000mg)** — Experienced users, higher dose needs

Higher concentration isn''t always better; it''s about matching your dose requirements.

### Red Flags to Avoid

- No [third-party testing](/glossary/third-party-testing) available
- Health claims ("cures," "treats," "heals")
- Prices that seem too good to be true
- No information about hemp source
- Unclear or missing ingredient lists
- MLM/multi-level marketing brands

## Frequently Asked Questions

### How long does CBD oil take to work?

When taken sublingually (under the tongue), CBD oil typically takes 15-30 minutes to produce noticeable effects. If swallowed directly without holding under the tongue, expect 30-60 minutes as it must pass through digestion. Peak effects usually occur 1-2 hours after taking.

### How much CBD oil should I take?

Start with 10-25mg and observe effects over several days before increasing. Many people find their effective dose between 25-75mg daily, though some use more. Factors like body weight, metabolism, and what you''re using it for all influence your ideal dose. Use our [dosage calculator](/tools/dosage-calculator) for guidance.

### Can I take CBD oil with food?

Yes. While sublingual absorption bypasses digestion, any CBD you swallow will absorb better with fatty foods. Taking CBD oil with breakfast or dinner is fine. Some people add it directly to food or drinks, though this converts it fully to an edible (slower onset, lower bioavailability).

### Does CBD oil expire?

Yes. Most CBD oils have a shelf life of 12-24 months when stored properly. Keep your bottle in a cool, dark place—not in direct sunlight or a hot car. Signs of expired CBD oil include cloudiness, off smell, or changed taste. The CBD won''t become dangerous, but potency decreases over time.

### Is CBD oil legal in Europe?

CBD oil is legal in most European countries when derived from hemp and containing less than 0.2% THC (0.3% in some countries). However, regulations vary by country, and CBD''s status under Novel Food regulations means some products may need authorisation. Check your country''s specific rules—see our [CBD legal status guides](/legal).

### What''s the difference between CBD oil and hemp seed oil?

CBD oil is extracted from hemp flowers, leaves, and stalks and contains cannabidiol. Hemp seed oil is pressed from hemp seeds and contains no CBD—it''s a nutritional oil rich in omega fatty acids. Some products use hemp seed oil as the carrier oil for CBD. Check labels carefully; "hemp oil" alone doesn''t guarantee CBD content.


## What is CBD Oil?

**CBD oil** is a liquid extract from the hemp plant combined with a carrier oil such as MCT (coconut), hemp seed, or olive oil. It contains [cannabidiol](/glossary/cannabidiol), a non-intoxicating compound that interacts with your body''s [endocannabinoid system](/glossary/endocannabinoid-system).

CBD oil comes in three main formulations:

1. **[Full-spectrum CBD](/glossary/full-spectrum-cbd)** — Contains CBD plus other cannabinoids (including trace THC under 0.2%), [terpenes](/glossary/terpenes), and flavonoids. These compounds may work together in what''s called the [entourage effect](/glossary/entourage-effect).

2. **[Broad-spectrum CBD](/glossary/broad-spectrum-cbd)** — Contains multiple cannabinoids and terpenes but with THC completely removed. A middle ground for those wanting plant compounds without any THC.

3. **[CBD isolate](/glossary/cbd-isolate)** — Pure CBD (99%+) mixed with carrier oil. No other cannabinoids or plant compounds. Best for those who need to avoid THC entirely.

Most CBD oils come in glass bottles with a dropper for measuring doses. Concentrations typically range from 250mg to 3000mg of CBD per bottle.

## How CBD Oil Works

When you place CBD oil under your tongue (sublingual administration), the CBD absorbs through the thin tissue directly into your bloodstream. This bypasses the digestive system and liver, which would otherwise break down much of the CBD before it reaches circulation.

**How sublingual absorption works:**
- CBD molecules pass through the mucous membrane under your tongue
- They enter the bloodstream directly via capillaries
- CBD then reaches endocannabinoid receptors throughout your body
- It interacts primarily with CB1 and CB2 receptors, plus serotonin and vanilloid receptors


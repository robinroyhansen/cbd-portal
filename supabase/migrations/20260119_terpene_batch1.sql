| **Thyme** | Culinary herb with medicinal history |
| **Bay leaves** | Aromatic cooking ingredient |
| **Wild thyme** | Traditional European folk medicine |

### In CBD Products

Myrcene-dominant CBD products tend to be:

- **[Full-spectrum CBD oils](/glossary/full-spectrum-cbd)** from indica-leaning hemp strains
- Products marketed for **relaxation or sleep**
- Hemp flower with **earthy, musky aromas**

Check the [Certificate of Analysis (COA)](/glossary/certificate-of-analysis) if available — some labs test for terpene profiles, listing myrcene content.

## Effects & Benefits

### What Research Suggests

Studies on myrcene indicate several potential therapeutic properties:

**Sedative and Muscle Relaxant**
A study in *Phytomedicine* found that myrcene increased sleep duration in mice at high doses. The compound appears to have muscle-relaxant and sedative properties, which may explain why myrcene-rich strains are associated with "couch-lock."

**Anti-inflammatory**
Research published in the *Journal of Ethnopharmacology* demonstrated myrcene''s anti-inflammatory effects in animal models, suggesting potential benefits for conditions involving inflammation.

**Analgesic (Pain-Relieving)**
Animal studies suggest myrcene may have pain-relieving properties, potentially working through different pathways than opioid medications.

**Enhanced Absorption**
Some research suggests myrcene may increase cell membrane permeability, potentially enhancing the absorption of [cannabinoids](/glossary/cannabinoid) and other compounds. This could partly explain the [entourage effect](/glossary/entourage-effect).

### Important Caveats

Most myrcene research uses isolated compounds in animal models or cell studies. Human clinical trials specifically on myrcene are limited. Effects observed in labs don''t always translate directly to consuming myrcene in CBD products.

## Myrcene and CBD: The Entourage Effect

Myrcene is thought to be a key player in the [entourage effect](/glossary/entourage-effect) — the theory that cannabis compounds work better together.

### How Myrcene May Enhance CBD

1. **Increased absorption** — May help CBD cross cell membranes more easily
2. **Complementary effects** — Myrcene''s sedative properties may enhance CBD''s calming effects
3. **Receptor modulation** — May influence how cannabinoids interact with receptors

### Practical Implications

If you''re using CBD for relaxation or sleep, choosing myrcene-rich [full-spectrum](/glossary/full-spectrum-cbd) products might enhance results compared to [CBD isolate](/glossary/cbd-isolate). However, individual responses vary significantly.

## The Mango Myth

You may have heard that eating mangoes before taking CBD enhances effects because mangoes contain myrcene.

**The reality:** While mangoes do contain myrcene, the amounts are relatively small compared to what you''d find in cannabis. There''s no scientific evidence that eating mangoes meaningfully increases CBD absorption or effects. It''s a popular claim, but treat it as anecdotal rather than proven.


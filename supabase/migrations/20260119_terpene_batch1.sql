## Frequently Asked Questions

### Does myrcene make you sleepy?

Research suggests myrcene has sedative properties at higher concentrations, which may contribute to drowsiness. However, the amounts in CBD products vary significantly. Products high in myrcene may promote relaxation, but effects depend on the individual and overall product formulation.

### Is myrcene safe?

Myrcene is found in many foods and has GRAS (Generally Recognised as Safe) status for food use. You consume myrcene regularly through mangoes, beer (hops), and herbs like thyme. At levels found in CBD products, myrcene is considered safe for most people.

### How can I tell if my CBD product has myrcene?

Check the Certificate of Analysis (COA) if terpene testing is included. Alternatively, smell the product — myrcene-rich CBD has an earthy, musky, herbal aroma rather than citrusy or floral notes.

### Does myrcene help with pain?

Animal studies suggest myrcene has analgesic (pain-relieving) properties. Combined with CBD''s potential pain-modulating effects, myrcene-rich products may be worth trying for pain — but human research specifically on myrcene for pain is limited.


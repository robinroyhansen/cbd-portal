## Quick Answer

**Myrcene** (β-myrcene) is a monoterpene with an earthy, musky aroma reminiscent of cloves and lemongrass. It''s the dominant terpene in most cannabis strains and is also found in mangoes, hops, and thyme. Research suggests myrcene may have sedating, muscle-relaxant, and anti-inflammatory properties. In CBD products, myrcene may contribute to relaxation and enhanced [cannabinoid](/glossary/cannabinoid) absorption.

## Key Takeaways

- Myrcene is typically the most abundant terpene in cannabis (20-50% of terpene content)
- Aroma: Earthy, musky, herbal — like cloves and ripe mangoes
- Found in mangoes, hops, lemongrass, thyme, and bay leaves
- Research suggests sedating and muscle-relaxant effects
- May enhance cannabinoid absorption through cell membranes
- Best for: Relaxation, sleep support, muscle tension

## Aroma & Flavour Profile

Myrcene has a complex scent that''s often described as:

- **Earthy and musky** — The foundation of cannabis''s characteristic smell
- **Herbal** — Similar to bay leaves and thyme
- **Slightly sweet** — Hints of ripe tropical fruit
- **Clove-like** — Warm, spicy undertones

When you smell a CBD oil with strong "hempy" or "dank" notes, myrcene is usually the dominant contributor. The aroma is grounding and warm — quite different from the bright citrus notes of [limonene](/articles/what-is-limonene) or the floral sweetness of [linalool](/articles/what-is-linalool).


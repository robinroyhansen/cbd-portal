## Frequently Asked Questions

### Does limonene give you energy?

Limonene doesn''t provide energy like caffeine. However, its mood-elevating and potentially anxiety-reducing properties may help you feel more alert and positive. The bright citrus aroma itself can be invigorating — aromatherapy research supports citrus scents for mood enhancement.

### Is limonene safe to consume?

Yes. Limonene has GRAS (Generally Recognised as Safe) status and is widely used in food and supplements. You consume limonene every time you eat citrus fruits or foods flavoured with citrus oils. The amounts in CBD products are well within safe limits.

### Can limonene help with anxiety?

Research suggests limonene may have anxiolytic (anti-anxiety) properties. Animal studies show reduced anxiety-like behaviour with limonene exposure. Combined with CBD, limonene-rich products may be particularly useful for anxiety — though individual responses vary.

### What''s the difference between D-limonene and L-limonene?

Limonene exists in two mirror-image forms. D-limonene (dextro-limonene) has the citrus smell and is the form found in oranges and cannabis. L-limonene has a piney, turpentine-like smell and is found in pine. CBD products contain D-limonene.


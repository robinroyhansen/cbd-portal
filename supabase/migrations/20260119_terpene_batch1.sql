**Limonene** is a cyclic monoterpene with a distinctive citrus aroma — think fresh lemon and orange peel. It''s the second most abundant terpene in nature (after [pinene](/articles/what-is-pinene)) and is found in citrus fruits, juniper, and cannabis. Research suggests limonene may have mood-elevating, anti-anxiety, and antifungal properties. In CBD products, it''s associated with energising, uplifting effects.


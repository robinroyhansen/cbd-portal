## Safety Considerations

Limonene is generally safe — you consume it regularly in citrus fruits and it''s used extensively in food flavouring, cleaning products, and cosmetics.

**Considerations:**
- **Skin sensitivity** — Some people experience irritation with high-concentration limonene topicals
- **Allergies** — Rare, but citrus allergies can extend to limonene

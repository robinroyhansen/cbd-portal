## Quick Answer

**Pinene** (α-pinene and β-pinene) is a bicyclic monoterpene with a fresh pine aroma. It''s the most common terpene in nature, found in pine needles, rosemary, basil, and cannabis. Research suggests pinene may promote alertness, improve memory retention, and act as a bronchodilator. Uniquely, pinene may counteract THC-induced memory impairment, making it valuable in [full-spectrum](/glossary/full-spectrum-cbd) CBD products.


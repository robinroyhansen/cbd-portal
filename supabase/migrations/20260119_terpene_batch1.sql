## Effects & Benefits

### What Research Suggests

Limonene has been studied for several potential therapeutic applications:

**Mood Elevation and Anti-Anxiety**
Research published in *Behavioural Brain Research* found that limonene inhalation reduced anxiety-like behaviour in mice. Human studies on limonene aromatherapy show promising results for mood and stress reduction.

**Antifungal and Antibacterial**
Limonene demonstrates significant antimicrobial activity, particularly against fungi. It''s used in natural cleaning products for this reason.

**Potential Anti-Cancer Properties**
Early research (primarily cell and animal studies) suggests limonene may have anti-tumour properties. Human trials are ongoing, particularly for breast cancer prevention. This is preliminary research — not a reason to use limonene for cancer treatment.

**Gastric Protection**
Some studies indicate limonene may help protect the stomach lining and could be useful for acid reflux. D-limonene supplements are sometimes used for heartburn.

**Enhanced Absorption**
Like other terpenes, limonene may improve the absorption of other compounds through the skin and mucous membranes, potentially enhancing [cannabinoid](/glossary/cannabinoid) uptake.

### Research Limitations

Most limonene studies use isolated compounds, often at concentrations higher than you''d find in CBD products. Human clinical trials specifically on limonene are limited, and results from cell/animal studies don''t always translate to human use.

## Limonene and CBD: The Entourage Effect

